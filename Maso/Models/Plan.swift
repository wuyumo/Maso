import Foundation

// 训练计划的一个步骤 (= web 端 PlanStep)
struct PlanStep: Identifiable, Hashable, Codable, Sendable {
    let id: String
    let exerciseId: String
    var sets: Int = 1
    var reps: Int?
    var weight: Double?
    /// 有氧 / 灵活性段的时长 (秒); strength 则为 nil
    var duration: Int?
    /// 组间休息 (秒)
    var restBetweenSets: Int = 90
    /// 步骤之间额外的休息 (秒); 默认 0
    var rest: Int = 0
}

// 训练计划 (= web 端 Plan)
struct Plan: Identifiable, Hashable, Codable, Sendable {
    let id: String
    var name: String
    var steps: [PlanStep]
    var createdAt: Date
    var updatedAt: Date
    /// 是否为系统自动生成 (推荐计划 / 自由训练即时合成的)
    var autoGenerated: Bool = false
    var lastUsedAt: Date?
}

// 历史记录 (= web 端 SetRecord)
struct SetRecord: Identifiable, Hashable, Codable, Sendable {
    let id: String
    let exerciseId: String
    let exerciseName: String
    let category: ExerciseCategory
    var weight: Double?
    var reps: Int?
    var duration: Int?
    let performedAt: Date
    var planId: String?
}
